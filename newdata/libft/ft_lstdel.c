#include <stdlib.h>
#include "libft.h"

void	ft_lstdel(t_list **alst, void (*del)(void *, size_t))
{
	t_list	*tmp;
	t_list	*node;

	if (!del || !alst)
		return ;
	node = *alst;
	while (node != NULL)
	{
		tmp = node->next;
		del(node->content, node->content_size);
		free(node);
		node = tmp;
	}
	*alst = NULL;
}
