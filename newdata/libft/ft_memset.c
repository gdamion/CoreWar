#include <string.h>

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned char *ptr;
	unsigned char uns_c;

	ptr = (unsigned char *)b;
	uns_c = (unsigned char)c;
	while (len--)
		*ptr++ = uns_c;
	return (b);
}
