#include <stdlib.h>

char	*ft_strchr(const char *s, int c)
{
	char *ret;

	ret = (char *)s;
	while (*ret != c)
	{
		if (*ret == '\0')
			return (NULL);
		ret++;
	}
	if (*ret == c)
		return (ret);
	return (ret);
}
